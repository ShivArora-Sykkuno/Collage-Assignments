public class pattern_7 {
    public static void main(String[] args) {
        System.out.println("Shiv Arora");
        for (int i = 1; i <= 5; i++) {
            for (int j = 0; j < i; j++) {
                System.out.print(i + " ");
            }
            System.out.println();
        }
    }
}
