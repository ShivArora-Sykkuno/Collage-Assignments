public class pattern_3 {
    public static void main(String[] args) {
        System.out.println("Shiv Arora");
        for (int i = 0; i < 4; i++) {
            for (char ch = 'A'; ch <= 'F'; ch++) {
                System.out.print(ch + " ");
            }
            System.out.println();
        }
    }
}
